char	*ft_strchr(const char *s, int c)
{
	int	i;

	if (!s)
		return (0);
	i = 0;
	while (s[i] && s[i] != c)
		i ++;
	if (s[i] == c)
		return ((char *) &(s[i]));
	return (0);
}
